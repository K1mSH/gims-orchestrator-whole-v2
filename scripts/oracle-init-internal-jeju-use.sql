-- 1. IF_RSV_TB_JEJU_JEWON (제주 관측점 마스터)
CREATE TABLE IF_RSV_TB_JEJU_JEWON (
    ID                NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    OBSRVT_ID         VARCHAR2(30),
    OBSRVT_NM         VARCHAR2(100),
    SPOT_NM           VARCHAR2(100),
    LO_VALUE          VARCHAR2(20),
    LA_VALUE          VARCHAR2(20),
    TMX_VALUE         NUMBER,
    TMY_VALUE         NUMBER,
    EXTN_CSNG_CALBR   VARCHAR2(5),
    BUNJI             VARCHAR2(40),
    SIGUN_NM          VARCHAR2(40),
    EMD_NM            VARCHAR2(40),
    HO                VARCHAR2(40),
    LI_NM             VARCHAR2(40),
    DRNK_AT           VARCHAR2(1),
    USE_AT            VARCHAR2(1),
    AL_VALUE          VARCHAR2(50),
    WAL               NUMBER,
    UGRWTR_PRPOS_CODE VARCHAR2(2),
    LEGALDONG_CODE    VARCHAR2(10),
    SOURCE_REFS       VARCHAR2(4000),
    LINK_STATUS       VARCHAR2(20),
    EXTRACTED_AT      TIMESTAMP,
    UPDATED_AT        TIMESTAMP,
    EXECUTION_ID      VARCHAR2(255),
    CONSTRAINT UK_IF_RSV_JEJU_JEWON_SR UNIQUE (SOURCE_REFS)
);

-- 2. IF_RSV_TB_JEJU (제주 수위 관측)
CREATE TABLE IF_RSV_TB_JEJU (
    ID                NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RID               NUMBER(19),
    OBSRVT_ID         VARCHAR2(30),
    YMD               VARCHAR2(8),
    DATA_TIME         VARCHAR2(6),
    GL                VARCHAR2(20),
    SCOND             VARCHAR2(20),
    WTEMP             VARCHAR2(20),
    MSN               VARCHAR2(20),
    SOURCE_REFS       VARCHAR2(4000),
    LINK_STATUS       VARCHAR2(20),
    EXTRACTED_AT      TIMESTAMP,
    UPDATED_AT        TIMESTAMP,
    EXECUTION_ID      VARCHAR2(255),
    CONSTRAINT UK_IF_RSV_TB_JEJU_SR UNIQUE (SOURCE_REFS)
);

-- 3. IF_RSV_USE_LEGACY_DATA (이용량 레거시)
CREATE TABLE IF_RSV_USE_LEGACY_DATA (
    ID                 NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SN                 NUMBER(19),
    TELNO              VARCHAR2(255),
    OBSR_DT            TIMESTAMP,
    LAST_MEASURE_VALUE NUMBER(19),
    USGQTY             NUMBER(19),
    LAST_CHANGE_DT     TIMESTAMP,
    SOURCE_REFS        VARCHAR2(4000),
    LINK_STATUS        VARCHAR2(20),
    EXTRACTED_AT       TIMESTAMP,
    UPDATED_AT         TIMESTAMP,
    EXECUTION_ID       VARCHAR2(255),
    CONSTRAINT UK_IF_RSV_USE_LEGACY_SR UNIQUE (SOURCE_REFS)
);

-- 4. IF_RSV_USE_STATUS_DATA (이용량 상태)
CREATE TABLE IF_RSV_USE_STATUS_DATA (
    ID                NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SN                NUMBER(19),
    TELNO             VARCHAR2(255),
    OBSR_DT           TIMESTAMP,
    LAST_CHANGE_DT    TIMESTAMP,
    STAT              VARCHAR2(255),
    SOURCE_REFS       VARCHAR2(4000),
    LINK_STATUS       VARCHAR2(20),
    EXTRACTED_AT      TIMESTAMP,
    UPDATED_AT        TIMESTAMP,
    EXECUTION_ID      VARCHAR2(255),
    CONSTRAINT UK_IF_RSV_USE_STATUS_SR UNIQUE (SOURCE_REFS)
);

-- 5. IF_RSV_USE_JEJU_DAY (제주 일일이용량)
CREATE TABLE IF_RSV_USE_JEJU_DAY (
    ID                NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    OBSRVT_ID         VARCHAR2(30),
    OBSR_DE           VARCHAR2(8),
    USGQTY            NUMBER,
    LAST_MESURE_VALUE NUMBER,
    FRST_MESURE_VALUE NUMBER,
    DTA_STTUS_CODE    VARCHAR2(2),
    SOURCE_REFS       VARCHAR2(4000),
    LINK_STATUS       VARCHAR2(20),
    EXTRACTED_AT      TIMESTAMP,
    UPDATED_AT        TIMESTAMP,
    EXECUTION_ID      VARCHAR2(255),
    CONSTRAINT UK_IF_RSV_USE_JEJUDAY_SR UNIQUE (SOURCE_REFS)
);

-- execution_id 인덱스 (추적 성능)
CREATE INDEX IDX_RSV_JEJU_JEWON_EID ON IF_RSV_TB_JEJU_JEWON(EXECUTION_ID);
CREATE INDEX IDX_RSV_TB_JEJU_EID ON IF_RSV_TB_JEJU(EXECUTION_ID);
CREATE INDEX IDX_RSV_USE_LEGACY_EID ON IF_RSV_USE_LEGACY_DATA(EXECUTION_ID);
CREATE INDEX IDX_RSV_USE_STATUS_EID ON IF_RSV_USE_STATUS_DATA(EXECUTION_ID);
CREATE INDEX IDX_RSV_USE_JEJUDAY_EID ON IF_RSV_USE_JEJU_DAY(EXECUTION_ID);
