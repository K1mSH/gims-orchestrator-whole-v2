-- =============================================
-- bojo-int 자체 DB 테이블 (Oracle 29004/XEPDB1)
-- sync_log + execution (PG에서 이전)
-- =============================================

-- sync_log
CREATE TABLE SYNC_LOG (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    EXECUTION_ID VARCHAR2(100) NOT NULL,
    STEP_ID VARCHAR2(50),
    MAPPING_NAME VARCHAR2(100),
    SOURCE_TABLES VARCHAR2(4000),
    TARGET_TABLES VARCHAR2(4000),
    READ_COUNT NUMBER,
    WRITE_COUNT NUMBER,
    FAILED_COUNT NUMBER,
    SKIP_COUNT NUMBER,
    FAILED_KEYS VARCHAR2(4000),
    ERROR_SUMMARY VARCHAR2(4000),
    SOURCE_PK_COLUMN VARCHAR2(100),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT PK_SYNC_LOG PRIMARY KEY (ID)
);
CREATE INDEX IDX_SYNC_LOG_EXEC ON SYNC_LOG (EXECUTION_ID);
CREATE INDEX IDX_SYNC_LOG_MAP ON SYNC_LOG (EXECUTION_ID, MAPPING_NAME);

-- execution
CREATE TABLE EXECUTION (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    EXECUTION_ID VARCHAR2(100) NOT NULL,
    AGENT_ID VARCHAR2(100),
    SOURCE_DATASOURCE_ID VARCHAR2(50),
    TARGET_DATASOURCE_ID VARCHAR2(50),
    PARENT_EXECUTION_ID VARCHAR2(100),
    STATUS VARCHAR2(20),
    STARTED_AT TIMESTAMP,
    FINISHED_AT TIMESTAMP,
    DURATION_MS NUMBER,
    TOTAL_READ_COUNT NUMBER,
    TOTAL_WRITE_COUNT NUMBER,
    TOTAL_SKIP_COUNT NUMBER,
    ERROR_MESSAGE VARCHAR2(4000),
    CONSTRAINT PK_EXECUTION PRIMARY KEY (ID),
    CONSTRAINT UK_EXECUTION_EID UNIQUE (EXECUTION_ID)
);
