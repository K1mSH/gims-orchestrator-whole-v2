-- VIEW_GTEST — 수질측정망 통합뷰 (개발용 물리 테이블 단순화)
-- 대상 API: opn.info_general_211215 (Type B6)
-- 운영 환경: 실제 Oracle 뷰 (운영 GIMS 측 정의)
-- 개발 환경: 핸들러 SQL 호환 위해 물리 테이블로 단순화
--
-- 핸들러 SQL 사용 컬럼만 (전체 101 컬럼 중) — 13개
-- PK: GENNUM (단건 조회용)
-- ──────────────────────────────────────────────────────────

-- 1. 테이블 생성
BEGIN
    EXECUTE IMMEDIATE '
        CREATE TABLE VIEW_GTEST (
            GENNUM      NUMBER(22)    NOT NULL,
            SIDO        VARCHAR2(40),
            SIGUNGU     VARCHAR2(40),
            UPMYUNDO    VARCHAR2(40),
            RI          VARCHAR2(40),
            ADDR        VARCHAR2(250),
            JOSACODE    NUMBER(5),
            JIGUNAME    VARCHAR2(100),
            GWMYR       VARCHAR2(16),
            WELLNUM     VARCHAR2(50),
            GROUNDUSE   VARCHAR2(100),
            DRINKOX     VARCHAR2(1),
            GUBUN       VARCHAR2(18),
            CONSTRAINT PK_VIEW_GTEST PRIMARY KEY (GENNUM)
        )
    ';
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE != -955 THEN RAISE; END IF;
END;
/

-- 2. 컬럼 주석
COMMENT ON TABLE  VIEW_GTEST           IS '수질측정망 통합뷰 (개발 단순화)';
