COMMENT ON COLUMN VIEW_GTEST.RI        IS '리';
COMMENT ON COLUMN VIEW_GTEST.ADDR      IS '상세주소';
COMMENT ON COLUMN VIEW_GTEST.JOSACODE  IS '조사코드';
COMMENT ON COLUMN VIEW_GTEST.JIGUNAME  IS '지구명';
COMMENT ON COLUMN VIEW_GTEST.GWMYR     IS '관측년월';
COMMENT ON COLUMN VIEW_GTEST.WELLNUM   IS '관정번호';
COMMENT ON COLUMN VIEW_GTEST.GROUNDUSE IS '지하수용도';
COMMENT ON COLUMN VIEW_GTEST.DRINKOX   IS '음용여부 (Y/N)';
COMMENT ON COLUMN VIEW_GTEST.GUBUN     IS '구분';

-- 3. 샘플 데이터 3건
