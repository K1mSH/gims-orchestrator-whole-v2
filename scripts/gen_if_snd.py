lines.append('    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP')
lines.append(');')
lines.append('')
lines.append('-- 2. IF_SND tables (16)')

for tbl in sorted(tables.keys()):
    cols = tables[tbl]
    if_tbl = f'IF_SND_{tbl}'
    lines.append('')
    lines.append(f'DROP TABLE {if_tbl} CASCADE CONSTRAINTS;')
    lines.append(f'CREATE TABLE {if_tbl} (')
    lines.append(f'    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,')
    for col_def in cols:
        lines.append(f'    {col_def},')
    lines.append(f'    SOURCE_REFS VARCHAR2(4000),')
    lines.append(f'    LINK_STATUS VARCHAR2(20) DEFAULT \'PENDING\',')
    lines.append(f'    EXTRACTED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,')
    lines.append(f'    UPDATED_AT TIMESTAMP,')
    lines.append(f'    EXECUTION_ID VARCHAR2(255),')
    lines.append(f'    CONSTRAINT PK_{if_tbl} PRIMARY KEY (ID),')
    lines.append(f'    CONSTRAINT UK_{if_tbl}_SR UNIQUE (SOURCE_REFS)')
    lines.append(');')
    lines.append(f'CREATE INDEX IDX_{if_tbl}_EID ON {if_tbl} (EXECUTION_ID);')

