import sys, os

meta_path = os.path.join(os.path.dirname(__file__), 'oracle_meta.txt')
tables = {}
with open(meta_path, 'r') as f:
    for line in f:
        line = line.strip()
        if not line or '|' not in line:
            continue
        parts = line.split('|')
        if len(parts) < 5:
            continue
        tbl, col, dtype, size, nullable = parts[0].strip(), parts[1].strip(), parts[2].strip(), parts[3].strip(), parts[4].strip()
        if tbl == 'LINK_PLAN':
            continue
        if tbl not in tables:
            tables[tbl] = []
        if dtype in ('VARCHAR2', 'CHAR'):
            col_def = f"{col} {dtype}({size})"
        elif dtype == 'NUMBER' and size:
            col_def = f"{col} NUMBER({size})"
        elif dtype == 'NUMBER':
            col_def = f"{col} NUMBER"
        elif dtype == 'DATE':
            col_def = f"{col} DATE"
        else:
            col_def = f"{col} {dtype}"
        tables[tbl].append(col_def)

lines = []
lines.append('-- =============================================')
lines.append('-- saeol-oracle-init.sql')
lines.append('-- Oracle 29005 (XEPDB1, K1M)')
lines.append('-- IF_SND 16 tables + LINK_PLAN_CURSOR + test data')
