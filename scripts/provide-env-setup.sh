log "Docker 확인 완료"

# ----- 2. Oracle 컨테이너 (29004) -----
start_container() {
    local name=$1 port=$2 image=$3
    shift 3
    if docker ps --format '{{.Names}}' | grep -q "^${name}$"; then
        log "${name} 이미 실행 중 (:${port})"
    elif docker ps -a --format '{{.Names}}' | grep -q "^${name}$"; then
        warn "${name} 시작 중..."
        docker start ${name}
    else
        warn "${name} 생성 중..."
        docker run -d --name ${name} -p ${port}:${3:-5432} "$@" ${image}
    fi
}

# Oracle
if docker ps --format '{{.Names}}' | grep -q "^${ORACLE_CONTAINER}$"; then
    log "${ORACLE_CONTAINER} 이미 실행 중 (:${ORACLE_PORT})"
